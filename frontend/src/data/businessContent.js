/**
 * Helper to get business content based on current pathname
 */
export const getContentByPath = (pathname) => {
    const key = ROUTE_MAP[pathname];
    if (!key) {
        // Fallback for dynamic routes like /game/:gameId
        const match = pathname.match(/\/game\/([^/]+)/);
        if (match) {
            const id = match[1];
            // Fix for 'coffee' vs 'coffee_tycoon'
            if (id === 'coffee') return BUSINESS_CONTENT.coffee_tycoon;
            return BUSINESS_CONTENT[id];
        }
        return null;
    }
    return BUSINESS_CONTENT[key];
};
